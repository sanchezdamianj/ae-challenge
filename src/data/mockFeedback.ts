function pick<T>(arr: T[], index: number): T {
  return arr[index % arr.length];
}

export function generateLargeDataset(count: number): Feedback[] {
  const base = new Date('2023-01-01').getTime();
  const year = 365 * 24 * 60 * 60 * 1000;

  return Array.from({ length: count }, (_, i) => ({
    id: `gen-${i + 1}`,
    customerName: `${pick(FIRST_NAMES, i * 3)} ${pick(LAST_NAMES, i * 7)}`,
    email: `user${i + 1}@example.com`,
    category: pick(CATEGORIES, i),
    message: pick(MESSAGES, i * 5),
    priority: pick(PRIORITIES, i * 2),
    createdAt: new Date(base + (i / count) * year).toISOString(),
    status: pick(STATUSES, i * 4),
  }));
}

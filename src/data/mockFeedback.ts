const CATEGORIES: FeedbackCategory[] = ['Bug', 'Feature Request', 'Billing', 'Other'];
const PRIORITIES: FeedbackPriority[] = ['Low', 'Medium', 'High'];
const STATUSES: FeedbackStatus[] = ['Open', 'In Progress', 'Resolved'];

const FIRST_NAMES = ['Alex', 'Blake', 'Casey', 'Dana', 'Ellis', 'Finley', 'Harper', 'Jordan', 'Morgan', 'Riley'];
